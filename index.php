<h2 style="text-align: center;"> O que é o Fivem? ou mais conhecido como RP (roleplay game) </h2>
<p style="text-align: center; font-family: Arial, Helvetica, sans-serif;">No FiveM, o modo mais popular é o Roleplay.
Nele, os jogadores interpretam um personagem como se estivessem vivendo em uma cidade real.
Exemplo:
Você pode ser um policial e fazer patrulhas.
Pode trabalhar como taxista.
Pode abrir uma empresa.
Pode participar de investigações ou assaltos (seguindo regras do servidor).
É quase como uma vida virtual simulada dentro do GTA V.</p>

<img src="img/FiveM-Symbol.png" alt="Imagem logo fivem">
<hr>

<h3 style="text-align: center;"> Os Servidores de FIVEM </h3>
<p>- No FiveM, servidores são espaços online criados por jogadores ou comunidades, onde você pode jogar GTA V de forma personalizada, diferente do GTA Online oficial.
Cada servidor pode ter regras, scripts e conteúdo próprios, tornando a experiência única.</p>
<hr>
 <p>- Diferente do GTA Online tradicional, o FiveM possibilita:</p>

 <p style="font-style: oblique; font-weight: bold;">🌎 Servidores customizados</p>
 <p style="font-style: oblique; font-weight: bold;">👮‍♂️ Roleplay (RP) – interpretar personagens como policial, médico, mecânico, criminoso etc.</p>
 <p style="font-style: oblique; font-weight: bold;">🚗 Carros e mapas modificados</p>
 <p style="font-style: oblique; font-weight: bold;">💬 Sistemas próprios de economia, empregos e regras</p>
 <p style="font-style: oblique; font-weight: bold;">🧩 Scripts exclusivos criados por desenvolvedores</p>
