    <section id="origem">
        <h2>1. Origem da tensão</h2>
        <p>
            FiveM é uma plataforma criada pela equipe <strong>Cfx.re</strong> que permite jogar 
            <em>GTA V</em> em servidores próprios, com mods e Roleplay. 
            Inicialmente, a <strong>Rockstar Games</strong> e sua controladora <strong>Take-Two Interactive</strong> 
            acusaram FiveM de facilitar pirataria, banindo contas de desenvolvedores relacionados ao projeto.
        </p>
    </section>

    <section id="mudanca">
        <h2>2. Rockstar muda de estratégia</h2>
        <p>
            Em 2022, a Rockstar publicou diretrizes oficiais sobre servidores de Roleplay, 
            reconhecendo parcialmente a importância da comunidade. Em agosto de 2023, a Rockstar comprou a equipe da Cfx.re, 
            tornando FiveM oficialmente parte da empresa.
        </p>
    </section>

    <section id="pos-aquisicao">
        <h2>3. Polêmica pós-aquisição</h2>
        <ul>
            <li><strong>Mudanças nas políticas de uso:</strong> regras mais rígidas sobre copyright, principalmente veículos e marcas.</li>
            <li><strong>Críticas sobre administração:</strong> saída de membros originais e instabilidade no gerenciamento.</li>
            <li><strong>Percepção de venda corporativa:</strong> sensação de perda da filosofia open-community.</li>
        </ul>
    </section>

    <section id="briga-atual">
        <h2>4. Situação atual</h2>
        <p>
            Hoje o conflito não é legal, mas há tensão entre fundamentos diferentes:
        </p>
        <ul>
            <li>Historicamente, a Rockstar tentou bloquear e acusar FiveM de ilegalidade.</li>
            <li>Após a compra, implementou regras mais restritivas, irritando parte da comunidade.</li>
        </ul>
    </section>

    <section id="impactos-futuros">
        <h2>5. Impactos recentes e futuros</h2>
